namespace Spryker\Glue\DocumentationGeneratorApi\Dependency\Client;

class DocumentationGeneratorApiToStorageClientBridge implements DocumentationGeneratorApiToStorageClientInterface
{
    /**
     * @var \Spryker\Client\Storage\StorageClientInterface
     */
    protected $storageClient;

    /**
     * @param \Spryker\Client\Storage\StorageClientInterface $storageClient
     */
    public function __construct($storageClient)
    {
        $this->storageClient = $storageClient;
    }

    /**
     * @param string $key
     * @param string $value
     * @param int|null $ttl
     *
     * @return void
     */
    public function set(string $key, string $value, ?int $ttl = null): void
    {
        $this->storageClient->set($key, $value, $ttl);
    }
}
